#!/usr/bin/env python3
"""Bricks database tool — the single door to a workspace's bricks.db.

Every read and write to workspace data goes through this script: skills and
agents never touch the database any other way (no raw sqlite3, no hand-written
SQL). Tables and columns are DYNAMIC, Clay-style: `add` creates the table on
first use, `add`/`modify` create missing columns on the fly. `_id` is the
reserved technical column (INTEGER PRIMARY KEY), generated by SQLite, never
set by callers. An enrichment column `X` is paired with `X_status` using the
shared vocabulary: pending | running | done | not_found | failed. Mutations
are all-or-nothing.

Callable both ways (like every Bricks tool):
    import db; db.select(path, "companies", where="...")   # function mode
    python3 db.py select companies --where "..."           # CLI mode

Database resolution: `--db <path>` explicit, otherwise the current workspace's
bricks.db (from ./bricks/config.json, override root with --root). `--db` /
`--root` may be placed BEFORE or AFTER the subcommand — both
`db.py --db X add ...` and `db.py add ... --db X` work.

CLI (JSON on stdout; on error JSON on stderr + exit 1):
    python3 db.py init
    python3 db.py tables
    python3 db.py schema  <table>
    python3 db.py select  <table> [--where SQL] [--cols a,b] [--limit N] [--offset N] [--order SQL]
    python3 db.py count   <table> [--where SQL]
    python3 db.py claim   <table> <status_col> [--limit 25] [--cols a,b] [--where SQL] [--retry-failed]
    python3 db.py add     <table> --rows '[{"name":"Acme"}]' [--key domain]
    python3 db.py modify  <table> --updates '[{"_id":3,"score":"A"}]'
    python3 db.py modify  <table> --set website_status=running --where "website_status='pending'"
    python3 db.py remove  <table> --ids '[3,7]'
    python3 db.py remove  <table> --where "status='disqualified'"
    python3 db.py drop-table <table> --confirm
    python3 db.py drop-column <table> <column>
    python3 db.py import-csv <table> <file.csv> [--key domain]

Pass '-' to --rows / --updates / --ids to read the JSON payload from stdin.
"""

from __future__ import annotations

import argparse
import csv
import json
import os
import re
import sqlite3
import sys

DEFAULT_SELECT_LIMIT = 50
ID = "_id"
#: Table and column names: plain identifiers only — everything else is quoted
#: data, never SQL. This is what makes dynamic DDL injection-safe. A leading _
#: is allowed so `_id` stays selectable; CREATING _-prefixed columns is still
#: rejected by the reserved-name checks.
IDENT_RE = re.compile(r"^[A-Za-z_][A-Za-z0-9_]*$")


class DbError(ValueError):
    """Raised on any invalid operation. The database is never modified."""


def resolve(db: str | None = None, root: str = "bricks") -> str:
    """Return the bricks.db path: explicit --db, or the current workspace's."""
    if db:
        return db
    sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
    import workspace
    try:
        return workspace.current_db(root)
    except workspace.WorkspaceError as exc:
        raise DbError(str(exc)) from None


def connect(path: str, create: bool = False) -> sqlite3.Connection:
    if not create and not os.path.isfile(path):
        raise DbError(f"database not found: {path} — run workspace.py new first")
    conn = sqlite3.connect(path)
    conn.execute("PRAGMA journal_mode=WAL")
    conn.execute("PRAGMA busy_timeout=5000")
    conn.row_factory = sqlite3.Row
    return conn


def init(path: str) -> dict:
    existed = os.path.isfile(path)
    os.makedirs(os.path.dirname(os.path.abspath(path)), exist_ok=True)
    connect(path, create=True).close()
    return {"ok": True, "action": "init", "db": os.path.abspath(path),
            "alreadyExisted": existed}


