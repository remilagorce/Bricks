def _load_json(value: str, flag: str):
    if value == "-":
        value = sys.stdin.read()
    try:
        return json.loads(value)
    except json.JSONDecodeError as exc:
        raise DbError(f"{flag}: invalid JSON ({exc})") from None


def _parse_set(pairs: list[str]) -> dict:
    sets = {}
    for pair in pairs:
        if "=" not in pair:
            raise DbError(f"--set expects col=value, got {pair!r}")
        col, value = pair.split("=", 1)
        sets[col.strip()] = value
    return sets


def main(argv=None) -> int:
    parser = argparse.ArgumentParser(
        description="Single door to a Bricks workspace database (JSON in, JSON out).")
    parser.add_argument("--root", default="bricks")
    parser.add_argument("--db", default=None, help="explicit bricks.db path")
    sub = parser.add_subparsers(dest="command", required=True)

    # `--db` / `--root` are global (declared on the top parser above), but a
    # bare argparse subparser rejects them AFTER the subcommand. The model
    # reliably writes --db last (field-tested friction), so accept it in BOTH
    # positions: every subparser inherits this flag group, with SUPPRESSED
    # defaults so omitting the flag after the subcommand never clobbers a
    # value given before it.
    dbflags = argparse.ArgumentParser(add_help=False)
    dbflags.add_argument("--root", default=argparse.SUPPRESS)
    dbflags.add_argument("--db", default=argparse.SUPPRESS)

    def add_cmd(name, **kw):
        return sub.add_parser(name, parents=[dbflags], **kw)

    add_cmd("init", help="create the database file if missing (WAL mode)")
    add_cmd("tables", help="list tables with row counts and columns")

    p = add_cmd("schema", help="show a table's columns")
    p.add_argument("table")

    p = add_cmd("select", help="read rows (JSON objects)")
    p.add_argument("table")
    p.add_argument("--where", default=None, help="SQL condition, e.g. \"status='pending'\"")
    p.add_argument("--cols", default=None, help="comma-separated column names")
    p.add_argument("--limit", type=int, default=None,
                   help=f"max rows (default {DEFAULT_SELECT_LIMIT}; -1 = no limit)")
    p.add_argument("--offset", type=int, default=0)
    p.add_argument("--order", default=None, help="ORDER BY clause, e.g. \"name ASC\"")

    p = add_cmd("count", help="count rows without reading them")
    p.add_argument("table")
    p.add_argument("--where", default=None)

    p = add_cmd("claim", help="atomically select pending rows and mark them running")
    p.add_argument("table")
    p.add_argument("status_col", help="the X_status column to claim on")
    p.add_argument("--limit", type=int, default=25, help="max rows to claim (default 25)")
    p.add_argument("--cols", default=None, help="columns to return (always includes _id)")
    p.add_argument("--where", default=None, help="extra SQL condition ANDed to the claim")
    p.add_argument("--retry-failed", action="store_true",
                   help="also claim 'failed' rows (explicit retry)")

    p = add_cmd("add", help="insert rows; creates table/columns as needed")
    p.add_argument("table")
    p.add_argument("--rows", required=True,
                   help="JSON list of objects, or '-' to read from stdin")
    p.add_argument("--key", default=None,
                   help="dedup column: rows whose key value already exists are skipped")

    p = add_cmd("modify", help="update cells by _id, or bulk --set with --where")
    p.add_argument("table")
    p.add_argument("--updates", default=None,
                   help='JSON list with "_id", or \'-\' for stdin')
    p.add_argument("--set", action="append", default=[], metavar="COL=VALUE",
                   help="bulk assignment (repeatable), requires --where")
    p.add_argument("--where", default=None)

    p = add_cmd("remove", help="delete rows by _id list or by --where")
    p.add_argument("table")
    p.add_argument("--ids", default=None, help="JSON list of _id values, or '-' for stdin")
    p.add_argument("--where", default=None)

    p = add_cmd("drop-table", help="permanently delete a table and all its rows")
    p.add_argument("table")
    p.add_argument("--confirm", action="store_true",
                   help="required acknowledgement — this cannot be undone")

    p = add_cmd("drop-column", help="drop one column, preserving all other columns and rows")
    p.add_argument("table")
    p.add_argument("column")

    p = add_cmd("import-csv", help="import a CSV file into a table")
    p.add_argument("table")
    p.add_argument("file")
    p.add_argument("--key", default=None, help="dedup column (same as add --key)")

    args = parser.parse_args(argv)
    try:
        path = resolve(args.db, args.root)
        if args.command == "init":
            result = init(path)
        elif args.command == "tables":
            result = tables(path)
        elif args.command == "schema":
            result = schema(path, args.table)
        elif args.command == "select":
            result = select(path, args.table, args.where, args.cols,
                            args.limit, args.offset, args.order)
        elif args.command == "count":
            result = count(path, args.table, args.where)
        elif args.command == "claim":
            result = claim(path, args.table, args.status_col, args.limit,
                           args.cols, args.where, args.retry_failed)
        elif args.command == "add":
            result = add(path, args.table, _load_json(args.rows, "--rows"), args.key)
        elif args.command == "modify":
            updates = _load_json(args.updates, "--updates") if args.updates else None
            sets = _parse_set(getattr(args, "set")) if getattr(args, "set") else None
            result = modify(path, args.table, updates, sets, args.where)
        elif args.command == "remove":
            ids = _load_json(args.ids, "--ids") if args.ids else None
            result = remove(path, args.table, ids, args.where)
        elif args.command == "drop-table":
            if not args.confirm:
                raise DbError("drop-table requires --confirm (this permanently deletes the table)")
            result = drop_table(path, args.table)
        elif args.command == "drop-column":
            result = drop_column(path, args.table, args.column)
        else:
            result = import_csv(path, args.table, args.file, args.key)
    except DbError as exc:
        print(json.dumps({"ok": False, "error": str(exc)}, ensure_ascii=False),
              file=sys.stderr)
        return 1
    print(json.dumps(result, ensure_ascii=False))
    return 0


