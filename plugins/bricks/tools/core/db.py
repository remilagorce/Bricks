def _check_ident(name, what: str) -> str:
    if not isinstance(name, str) or not IDENT_RE.match(name):
        raise DbError(f"invalid {what} {name!r}: letters, digits and _ only")
    if what == "table name" and name.startswith("_"):
        raise DbError(f"invalid table name {name!r}: must not start with _")
    return name


def _q(name: str) -> str:
    return f'"{name}"'


def _check_where(where: str | None) -> str | None:
    if where is None:
        return None
    if not isinstance(where, str) or not where.strip() or ";" in where:
        raise DbError("--where must be a single non-empty SQL condition (no ';')")
    return where.strip()


def _cell(value):
    """Normalize a JSON/Python value into a storable cell."""
    if isinstance(value, bool):
        return "true" if value else "false"
    if value is None or isinstance(value, (int, float, str)):
        return value
    return json.dumps(value, ensure_ascii=False)


def _check_objects(items, name: str) -> None:
    if not isinstance(items, list) or not items or not all(isinstance(i, dict) for i in items):
        raise DbError(f"{name} must be a non-empty JSON list of objects")
    for obj in items:
        for key in obj:
            if key != ID:
                _check_ident(key, "column name")
                if key.startswith("_"):
                    raise DbError(f"column {key!r}: _-prefixed names are reserved")
    if name == "rows" and any(ID in o for o in items):
        raise DbError(f'"{ID}" is generated by the database — remove it from your rows')


def _table_exists(conn, table: str) -> bool:
    return conn.execute("SELECT 1 FROM sqlite_master WHERE type='table' AND name=?",
                        (table,)).fetchone() is not None


def _require_table(conn, table: str) -> None:
    _check_ident(table, "table name")
    if not _table_exists(conn, table):
        available = [r["name"] for r in conn.execute(
            "SELECT name FROM sqlite_master WHERE type='table' "
            "AND name NOT LIKE 'sqlite_%' ORDER BY name")]
        raise DbError(f"table {table!r} not found — existing tables: {available or 'none'}")


def columns(conn, table: str) -> list[str]:
    return [r["name"] for r in conn.execute(f"PRAGMA table_info({_q(table)})")]


def _ensure_columns(conn, table: str, wanted: list[str]) -> list[str]:
    existing = set(columns(conn, table))
    added = []
    for col in wanted:
        if col not in existing:
            _check_ident(col, "column name")
            try:
                conn.execute(f"ALTER TABLE {_q(table)} ADD COLUMN {_q(col)} TEXT")
                added.append(col)
            except sqlite3.OperationalError as exc:
                # parallel runner workers can race on the same new column
                if "duplicate column" not in str(exc).lower():
                    raise
            existing.add(col)
    return added


def _ids_of(values, table: str) -> list[int]:
    """Coerce a list of ids (ints or numeric strings) to ints."""
    out = []
    for value in values:
        if isinstance(value, bool) or not isinstance(value, (int, str)):
            raise DbError(f"invalid {ID} {value!r} for {table}: must be an integer")
        try:
            out.append(int(value))
        except (TypeError, ValueError):
            raise DbError(f"invalid {ID} {value!r} for {table}: must be an integer") from None
    return out


def _require_ids_exist(conn, table: str, ids: list[int]) -> None:
    placeholders = ",".join("?" * len(ids))
    found = {r[ID] for r in conn.execute(
        f"SELECT {_q(ID)} FROM {_q(table)} WHERE {_q(ID)} IN ({placeholders})", ids)}
    missing = sorted(set(ids) - found)
    if missing:
        raise DbError(f"{ID}(s) not found in {table}: {missing} — nothing was modified")


