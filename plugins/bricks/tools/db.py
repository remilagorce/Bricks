def init(path: str) -> dict:
    existed = os.path.isfile(path)
    directory = os.path.dirname(os.path.abspath(path))
    os.makedirs(directory, exist_ok=True)
    conn = connect(path, create=True)
    conn.close()
    return {"ok": True, "action": "init", "db": os.path.abspath(path),
            "alreadyExisted": existed}


def tables(path: str) -> dict:
    conn = connect(path)
    try:
        names = [r["name"] for r in conn.execute(
            "SELECT name FROM sqlite_master WHERE type='table' AND name NOT LIKE 'sqlite_%' ORDER BY name"
        )]
        entries = [
            {"table": n,
             "rows": conn.execute(f"SELECT COUNT(*) AS c FROM {_q(n)}").fetchone()["c"],
             "columns": _columns(conn, n)}
            for n in names
        ]
    finally:
        conn.close()
    return {"ok": True, "db": os.path.abspath(path), "tables": entries}


def schema(path: str, table: str) -> dict:
    conn = connect(path)
    try:
        _require_table(conn, table)
        cols = _columns(conn, table)
        count = conn.execute(f"SELECT COUNT(*) AS c FROM {_q(table)}").fetchone()["c"]
    finally:
        conn.close()
    return {"ok": True, "table": table, "columns": cols, "rows": count}


def select(path: str, table: str, where: str | None = None, cols: str | None = None,
           limit: int | None = None, offset: int = 0, order: str | None = None) -> dict:
    conn = connect(path)
    try:
        _require_table(conn, table)
        where = _check_where(where)
        if cols:
            names = [_check_ident(c.strip(), "column name") for c in cols.split(",") if c.strip()]
            existing = set(_columns(conn, table))
            unknown = [c for c in names if c not in existing]
            if unknown:
                raise DbError(f"unknown column(s) {unknown} in {table} — columns: {sorted(existing)}")
            selection = ", ".join(_q(c) for c in names)
        else:
            selection = "*"
        query = f"SELECT {selection} FROM {_q(table)}"
        if where:
            query += f" WHERE {where}"
        if order:
            if ";" in order:
                raise DbError("--order must be a plain ORDER BY clause (no ';')")
            query += f" ORDER BY {order}"
        effective_limit = DEFAULT_SELECT_LIMIT if limit is None else limit
        if effective_limit >= 0:
            query += f" LIMIT {int(effective_limit)} OFFSET {int(offset)}"
        try:
            rows = [dict(r) for r in conn.execute(query)]
        except sqlite3.Error as exc:
            raise DbError(f"query failed ({exc}) — query was: {query}") from None
        total = conn.execute(
            f"SELECT COUNT(*) AS c FROM {_q(table)}" + (f" WHERE {where}" if where else "")
        ).fetchone()["c"]
    finally:
        conn.close()
    return {"ok": True, "table": table, "returned": len(rows), "matching": total,
            "limit": effective_limit, "rows": rows}


def count(path: str, table: str, where: str | None = None) -> dict:
    conn = connect(path)
    try:
        _require_table(conn, table)
        where = _check_where(where)
        query = f"SELECT COUNT(*) AS c FROM {_q(table)}" + (f" WHERE {where}" if where else "")
        try:
            total = conn.execute(query).fetchone()["c"]
        except sqlite3.Error as exc:
            raise DbError(f"query failed ({exc}) — query was: {query}") from None
    finally:
        conn.close()
    return {"ok": True, "table": table, "count": total}


def claim(path: str, table: str, status_col: str, limit: int = 25,
          cols: str | None = None, where: str | None = None,
          retry_failed: bool = False) -> dict:
    """Atomically select up to `limit` pending rows and mark them running.

    One command instead of the select-then-modify pair, inside a single
    BEGIN IMMEDIATE transaction: the write lock is taken BEFORE the select,
    so two parallel runs can never claim the same rows (the two-step
    pattern had that race). Rows whose row-level `status` is
    'disqualified' are never claimed (§5/§8); `--retry-failed` widens the
    scope to 'failed' rows on an explicit retry (iron rule 2).
    """
    _check_ident(status_col, "column name")
    if limit <= 0:
        raise DbError("--limit must be a positive integer")
    conn = connect(path)
    try:
        _require_table(conn, table)
        existing = _columns(conn, table)
        if status_col not in existing:
            raise DbError(f"column {status_col!r} not found in {table} — "
                          f"initialize it to 'pending' first (columns: {sorted(existing)})")
        if cols:
            names = [_check_ident(c.strip(), "column name") for c in cols.split(",") if c.strip()]
            unknown = [c for c in names if c not in existing]
            if unknown:
                raise DbError(f"unknown column(s) {unknown} in {table} — columns: {sorted(existing)}")
            if ID_COLUMN not in names:
                names.insert(0, ID_COLUMN)
            selection = ", ".join(_q(c) for c in names)
        else:
            selection = "*"
        statuses = "('pending','failed')" if retry_failed else "('pending')"
        cond = f"{_q(status_col)} IN {statuses}"
        if "status" in existing:
            cond += " AND (status IS NULL OR status!='disqualified')"
        extra = _check_where(where)
        if extra:
            cond += f" AND ({extra})"

        old_isolation = conn.isolation_level
        conn.isolation_level = None  # manual transaction: BEGIN IMMEDIATE
        try:
            conn.execute("BEGIN IMMEDIATE")
            try:
                ids = [r[ID_COLUMN] for r in conn.execute(
                    f"SELECT {_q(ID_COLUMN)} FROM {_q(table)} WHERE {cond} "
                    f"ORDER BY {_q(ID_COLUMN)} LIMIT ?", (int(limit),))]
            except sqlite3.Error as exc:
                raise DbError(f"claim failed ({exc}) — condition was: {cond}") from None
            rows = []
            if ids:
                placeholders = ",".join("?" * len(ids))
                conn.execute(
                    f"UPDATE {_q(table)} SET {_q(status_col)}='running' "
                    f"WHERE {_q(ID_COLUMN)} IN ({placeholders})", ids)
                rows = [dict(r) for r in conn.execute(
                    f"SELECT {selection} FROM {_q(table)} "
                    f"WHERE {_q(ID_COLUMN)} IN ({placeholders}) "
                    f"ORDER BY {_q(ID_COLUMN)}", ids)]
            conn.execute("COMMIT")
        except BaseException:
            conn.execute("ROLLBACK")
            raise
        finally:
            conn.isolation_level = old_isolation
    finally:
        conn.close()
    return {"ok": True, "action": "claim", "table": table,
            "statusCol": status_col, "claimed": len(rows), "rows": rows}


def add(path: str, table: str, rows: list[dict], key: str | None = None) -> dict:
    _check_ident(table, "table name")
    _check_objects(rows, "rows")
    _reject_reserved(rows, "rows")
    ordered_cols: list[str] = []
    for r in rows:
        for k in r:
            _check_ident(k, "column name")
            if k not in ordered_cols:
                ordered_cols.append(k)
    if key:
        _check_ident(key, "key column")
        if key not in ordered_cols:
            raise DbError(f"--key {key!r} must be present in the rows being added")

    conn = connect(path)
    try:
        with conn:
            created = not _table_exists(conn, table)
            if created:
                cols_sql = ", ".join(f"{_q(c)} TEXT" for c in ordered_cols)
                conn.execute(
                    f"CREATE TABLE {_q(table)} "
                    f"({_q(ID_COLUMN)} INTEGER PRIMARY KEY AUTOINCREMENT, {cols_sql})"
                )
                new_columns = ordered_cols
            else:
                new_columns = _ensure_columns(conn, table, ordered_cols)

            skipped = 0
            if key:
                seen = {
                    r["v"] for r in conn.execute(
                        f"SELECT {_q(key)} AS v FROM {_q(table)} WHERE {_q(key)} IS NOT NULL"
                    )
                }
                unique_rows = []
                for r in rows:
                    value = _cell(r.get(key))
                    if value is not None and value in seen:
                        skipped += 1
                        continue
                    if value is not None:
                        seen.add(value)
                    unique_rows.append(r)
                rows = unique_rows

            for r in rows:
                cols = list(r.keys())
                placeholders = ",".join("?" * len(cols))
                conn.execute(
                    f"INSERT INTO {_q(table)} ({', '.join(_q(c) for c in cols)}) "
                    f"VALUES ({placeholders})",
                    [_cell(r[c]) for c in cols],
                )
            total = conn.execute(f"SELECT COUNT(*) AS c FROM {_q(table)}").fetchone()["c"]
    finally:
        conn.close()
    return {"ok": True, "action": "add", "table": table, "created": created,
            "added": len(rows), "skippedDuplicates": skipped,
            "newColumns": new_columns, "rows": total}


def modify(path: str, table: str, updates: list[dict] | None = None,
           sets: dict | None = None, where: str | None = None) -> dict:
    conn = connect(path)
    try:
        _require_table(conn, table)
        if updates is not None:
            _check_objects(updates, "updates")
            _reject_reserved(updates, "updates")
            for u in updates:
                if ID_COLUMN not in u:
                    raise DbError(f'every update object needs a "{ID_COLUMN}" key, got: {u}')
                if len(u) < 2:
                    raise DbError(f"update for {ID_COLUMN}={u[ID_COLUMN]} has no columns to set")
            ids = _ids_of([u[ID_COLUMN] for u in updates], table)
            wanted = []
            for u in updates:
                for k in u:
                    if k != ID_COLUMN and k not in wanted:
                        _check_ident(k, "column name")
                        wanted.append(k)
            with conn:
                _require_ids_exist(conn, table, ids)
                new_columns = _ensure_columns(conn, table, wanted)
                updated_cells = 0
                for u, row_id in zip(updates, ids):
                    cols = [k for k in u if k != ID_COLUMN]
                    assignments = ", ".join(f"{_q(c)}=?" for c in cols)
                    conn.execute(
                        f"UPDATE {_q(table)} SET {assignments} WHERE {_q(ID_COLUMN)}=?",
                        [_cell(u[c]) for c in cols] + [row_id],
                    )
                    updated_cells += len(cols)
            return {"ok": True, "action": "modify", "table": table,
                    "updatedRows": len(set(ids)), "updatedCells": updated_cells,
                    "newColumns": new_columns}

        # bulk mode: --set col=value ... --where "..."
        if not sets:
            raise DbError("modify needs --updates, or --set with --where")
        where = _check_where(where)
        if not where:
            raise DbError("bulk --set requires --where (refusing to update every row blindly; "
                          "use --where 1=1 to really target all rows)")
        for k in sets:
            _check_ident(k, "column name")
            if k.startswith("_"):
                raise DbError(f"column {k!r} is reserved — technical columns cannot be set")
        with conn:
            new_columns = _ensure_columns(conn, table, list(sets.keys()))
            assignments = ", ".join(f"{_q(c)}=?" for c in sets)
            try:
                cursor = conn.execute(
                    f"UPDATE {_q(table)} SET {assignments} WHERE {where}",
                    [_cell(v) for v in sets.values()],
                )
            except sqlite3.Error as exc:
                raise DbError(f"update failed ({exc}) — where was: {where}") from None
            updated = cursor.rowcount
        return {"ok": True, "action": "modify", "table": table,
                "updatedRows": updated, "newColumns": new_columns, "where": where}
    finally:
        conn.close()


def remove(path: str, table: str, ids: list | None = None, where: str | None = None) -> dict:
    if (ids is None) == (where is None):
        raise DbError("remove needs exactly one of --ids or --where")
    conn = connect(path)
    try:
        _require_table(conn, table)
        with conn:
            if ids is not None:
                if not isinstance(ids, list) or not ids:
                    raise DbError("--ids must be a non-empty JSON list of _id values")
                int_ids = _ids_of(ids, table)
                _require_ids_exist(conn, table, int_ids)
                placeholders = ",".join("?" * len(int_ids))
                cursor = conn.execute(
                    f"DELETE FROM {_q(table)} WHERE {_q(ID_COLUMN)} IN ({placeholders})",
                    int_ids,
                )
            else:
                where = _check_where(where)
                try:
                    cursor = conn.execute(f"DELETE FROM {_q(table)} WHERE {where}")
                except sqlite3.Error as exc:
                    raise DbError(f"delete failed ({exc}) — where was: {where}") from None
            removed = cursor.rowcount
            total = conn.execute(f"SELECT COUNT(*) AS c FROM {_q(table)}").fetchone()["c"]
    finally:
        conn.close()
    return {"ok": True, "action": "remove", "table": table,
            "removed": removed, "rows": total}


def drop_table(path: str, table: str) -> dict:
    conn = connect(path)
    try:
        _require_table(conn, table)
        row_count = conn.execute(f"SELECT COUNT(*) AS c FROM {_q(table)}").fetchone()["c"]
        with conn:
            conn.execute(f"DROP TABLE {_q(table)}")
    finally:
        conn.close()
    return {"ok": True, "action": "drop-table", "table": table, "rowsDropped": row_count}


def drop_column(path: str, table: str, column: str) -> dict:
    """Drop a column, preserving every other column and every row.

    Uses native `ALTER TABLE ... DROP COLUMN` when the running SQLite
    supports it (3.35.0+); otherwise falls back to the standard SQLite
    recipe of recreating the table without that column inside a single
    transaction (rows, `_id` values and remaining columns untouched).
    """
    _check_ident(column, "column name")
    if column == ID_COLUMN:
        raise DbError(f'"{ID_COLUMN}" is the reserved primary key — it cannot be dropped')
    conn = connect(path)
    try:
        _require_table(conn, table)
        existing = _columns(conn, table)
        if column not in existing:
            raise DbError(f"column {column!r} not found in {table} — columns: {existing}")
        remaining = [c for c in existing if c != column]
        if len(remaining) <= 1:
            raise DbError(f"cannot drop {column!r}: {table} would be left with no data columns")
        with conn:
            try:
                conn.execute(f"ALTER TABLE {_q(table)} DROP COLUMN {_q(column)}")
            except sqlite3.OperationalError:
                # Older SQLite without DROP COLUMN support: recreate the
                # table without the column, copying every row across.
                tmp = f"{table}__drop_tmp"
                conn.execute(f"DROP TABLE IF EXISTS {_q(tmp)}")
                cols_sql = ", ".join(
                    f"{_q(ID_COLUMN)} INTEGER PRIMARY KEY AUTOINCREMENT" if c == ID_COLUMN
                    else f"{_q(c)} TEXT"
                    for c in remaining
                )
                conn.execute(f"CREATE TABLE {_q(tmp)} ({cols_sql})")
                col_list = ", ".join(_q(c) for c in remaining)
                conn.execute(
                    f"INSERT INTO {_q(tmp)} ({col_list}) SELECT {col_list} FROM {_q(table)}"
                )
                conn.execute(f"DROP TABLE {_q(table)}")
                conn.execute(f"ALTER TABLE {_q(tmp)} RENAME TO {_q(table)}")
            new_columns = _columns(conn, table)
            total = conn.execute(f"SELECT COUNT(*) AS c FROM {_q(table)}").fetchone()["c"]
    finally:
        conn.close()
    return {"ok": True, "action": "drop-column", "table": table,
            "droppedColumn": column, "columns": new_columns, "rows": total}


def import_csv(path: str, table: str, file: str, key: str | None = None) -> dict:
    if not os.path.isfile(file):
        raise DbError(f"file not found: {file}")
    with open(file, newline="", encoding="utf-8-sig") as f:
        reader = csv.DictReader(f)
        if not reader.fieldnames:
            raise DbError(f"empty CSV (no header row): {file}")
        rows = []
        for raw in reader:
            row = {k: v for k, v in raw.items()
                   if k and k != ID_COLUMN and not k.startswith("_")}
            if any((v or "").strip() for v in row.values()):
                rows.append(row)
    if not rows:
        raise DbError(f"no data rows in {file}")
    result = add(path, table, rows, key=key)
    result["action"] = "import-csv"
    result["file"] = file
    return result


