#!/usr/bin/env python3
"""Bricks database tool — the single door to a workspace's bricks.db.

Every read and write to workspace data goes through this script. Skills and
agents NEVER touch the database any other way (no raw sqlite3, no ORM, no
hand-written SQL in the conversation). The front server imports this module,
so the UI and the skills share exactly the same code paths.

Design contract (see CONVENTIONS.md at the plugin root):
- One SQLite database per workspace: `<workspace>/bricks.db`, WAL mode, so
  several writers (subagents in parallel) can work safely.
- Tables and columns are DYNAMIC, Clay-style: `add` creates the table on
  first use, `add`/`modify` create missing columns on the fly (ALTER TABLE).
- `_id` is the reserved technical column (INTEGER PRIMARY KEY): generated by
  SQLite, never set by callers, never displayed by the front. Rows are
  addressed by `_id` — stable under concurrent inserts and deletes.
- An enrichment column `X` is paired with `X_status` using the shared
  vocabulary: pending | running | done | not_found | failed.
- Mutations are all-or-nothing: any validation error aborts the whole
  command before anything is written (exit code != 0 means "not modified").

Database resolution: `--db <path>` explicit, otherwise the current
workspace's bricks.db (from ./bricks/config.json, override root with --root).
`--db` / `--root` may be placed BEFORE or AFTER the subcommand — both
`db.py --db X add …` and `db.py add … --db X` work.

CLI (JSON on stdout; on error JSON on stderr + exit 1):
    python3 db.py init
    python3 db.py tables
    python3 db.py schema  <table>
    python3 db.py select  <table> [--where SQL] [--cols a,b] [--limit N] [--offset N] [--order SQL]
    python3 db.py count   <table> [--where SQL]
    python3 db.py claim   <table> <status_col> [--limit 25] [--cols a,b] [--where SQL] [--retry-failed]
    python3 db.py add     <table> --rows '[{"name": "Acme"}]'        [--key domain]
    python3 db.py modify  <table> --updates '[{"_id": 3, "score": "A"}]'
    python3 db.py modify  <table> --set website_status=running --where "website_status='pending'"
    python3 db.py remove  <table> --ids '[3, 7]'
    python3 db.py remove  <table> --where "status='disqualified'"
    python3 db.py drop-table <table> --confirm
    python3 db.py drop-column <table> <column>
    python3 db.py import-csv <table> <file.csv> [--key domain]

Pass '-' to --rows / --updates / --ids to read the JSON payload from stdin.
"""

from __future__ import annotations

import argparse
import csv
import json
import os
import re
import sqlite3
import sys

DB_FILENAME = "bricks.db"
ID_COLUMN = "_id"
DEFAULT_SELECT_LIMIT = 50

#: Table and column names: plain identifiers only — everything else is
#: quoted data, never SQL. This is what makes dynamic DDL injection-safe.
#: A leading _ is allowed so `_id` stays selectable; CREATING _-prefixed
#: columns is still rejected by the reserved-name checks.
IDENT_RE = re.compile(r"^[A-Za-z_][A-Za-z0-9_]*$")


class DbError(ValueError):
    """Raised on any invalid operation. The database is never modified."""


# --------------------------------------------------------------------------
# Resolution & connection
# --------------------------------------------------------------------------

def resolve_db_path(root: str = "bricks", db: str | None = None) -> str:
    """Return the bricks.db path: explicit --db, or the current workspace's."""
    if db:
        return db
    sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
    import workspace as ws
    status = ws.status(root)
    if not status.get("initialized"):
        raise DbError(
            f"Bricks not initialized at {os.path.abspath(root)} — "
            "run workspace.py init / new first"
        )
    current = status.get("current")
    if not current:
        raise DbError(
            "no current workspace — run workspace.py new <name> or switch <name>"
        )
    return os.path.join(current["path"], DB_FILENAME)


def connect(path: str, create: bool = False) -> sqlite3.Connection:
    if not create and not os.path.isfile(path):
        raise DbError(f"database not found: {path} — run `init` (or workspace.py new)")
    conn = sqlite3.connect(path)
    conn.execute("PRAGMA journal_mode=WAL")
    conn.execute("PRAGMA busy_timeout=5000")
    conn.row_factory = sqlite3.Row
    return conn


# --------------------------------------------------------------------------
# Internals
# --------------------------------------------------------------------------

def _check_ident(name: str, what: str) -> str:
    if not isinstance(name, str) or not IDENT_RE.match(name):
        raise DbError(f"invalid {what} {name!r}: letters, digits and _ only")
    if what == "table name" and name.startswith("_"):
        raise DbError(f"invalid table name {name!r}: must not start with _")
    return name


def _q(name: str) -> str:
    return f'"{name}"'


def _check_where(where: str | None) -> str | None:
    if where is None:
        return None
    if not isinstance(where, str) or not where.strip():
        raise DbError("--where must be a non-empty SQL condition")
    if ";" in where:
        raise DbError("--where must be a single condition (no ';')")
    return where.strip()


def _cell(value):
    """Normalize a JSON/Python value into a storable cell."""
    if value is None or isinstance(value, (int, float, str)):
        if isinstance(value, bool):
            return "true" if value else "false"
        return value
    if isinstance(value, bool):
        return "true" if value else "false"
    return json.dumps(value, ensure_ascii=False)


def _check_objects(items, name: str) -> None:
    if not isinstance(items, list) or not items or not all(isinstance(i, dict) for i in items):
        raise DbError(f"{name} must be a non-empty JSON list of objects")


def _reject_reserved(objects: list[dict], what: str) -> None:
    for o in objects:
        for key in o:
            if key != ID_COLUMN and isinstance(key, str) and key.startswith("_"):
                raise DbError(
                    f"column {key!r}: names starting with _ are reserved for "
                    f"Bricks technical columns — remove it from your {what}"
                )
    if what == "rows" and any(ID_COLUMN in o for o in objects):
        raise DbError(
            f'"{ID_COLUMN}" is generated by the database — remove it from your rows'
        )


def _table_exists(conn: sqlite3.Connection, table: str) -> bool:
    row = conn.execute(
        "SELECT 1 FROM sqlite_master WHERE type='table' AND name=?", (table,)
    ).fetchone()
    return row is not None


def _require_table(conn: sqlite3.Connection, table: str) -> None:
    _check_ident(table, "table name")
    if not _table_exists(conn, table):
        available = [r["name"] for r in conn.execute(
            "SELECT name FROM sqlite_master WHERE type='table' AND name NOT LIKE 'sqlite_%' ORDER BY name"
        )]
        raise DbError(f"table {table!r} not found — existing tables: {available or 'none'}")


def _columns(conn: sqlite3.Connection, table: str) -> list[str]:
    return [r["name"] for r in conn.execute(f"PRAGMA table_info({_q(table)})")]


def _ensure_columns(conn: sqlite3.Connection, table: str, wanted: list[str]) -> list[str]:
    """ALTER TABLE ADD COLUMN for every column not yet in the table."""
    existing = set(_columns(conn, table))
    added = []
    for col in wanted:
        if col in existing:
            continue
        _check_ident(col, "column name")
        conn.execute(f"ALTER TABLE {_q(table)} ADD COLUMN {_q(col)} TEXT")
        existing.add(col)
        added.append(col)
    return added


def _ids_of(updates_or_ids, table: str) -> list[int]:
    """Coerce a list of ids (ints or numeric strings) to ints."""
    out = []
    for value in updates_or_ids:
        if isinstance(value, bool) or not isinstance(value, (int, str)):
            raise DbError(f"invalid {ID_COLUMN} {value!r} for {table}: must be an integer")
        try:
            out.append(int(value))
        except (TypeError, ValueError):
            raise DbError(f"invalid {ID_COLUMN} {value!r} for {table}: must be an integer") from None
    return out


def _require_ids_exist(conn: sqlite3.Connection, table: str, ids: list[int]) -> None:
    placeholders = ",".join("?" * len(ids))
    found = {r[ID_COLUMN] for r in conn.execute(
        f"SELECT {_q(ID_COLUMN)} FROM {_q(table)} WHERE {_q(ID_COLUMN)} IN ({placeholders})",
        ids,
    )}
    missing = sorted(set(ids) - found)
    if missing:
        raise DbError(f"{ID_COLUMN}(s) not found in {table}: {missing} — nothing was modified")


